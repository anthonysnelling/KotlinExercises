fun main(args: Array<String>){
/*
      Variables = a little bucket where we can put information
      Types: String, Int, Float, Double, Boolean, Char
      int = number
      double = 23.00 43.00
      boolean = true or false
      float = fraction 1.4, 0.6, 2.1
      Char = character = h,b, $, & ...
*/

    var myChar = 'c'
    var newChar: Char?= null
    newChar = 'B'

    println(myChar)
    println(newChar)
    println("$myChar $newChar")
}